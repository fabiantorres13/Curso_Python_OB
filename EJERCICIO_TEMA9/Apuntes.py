# Funciones para tener en cuenta

# 1. Filter -> Filtra los resultados de una lista. ESTRUCTURA -> filter(Funcion,lista)
# 2. Map -> Aplica una función a una lista. ESTRUCTURA -> map(Funcion, lista)
# 3. Reduce -> Aplica una función a los elementos de una lista y devuelve un solo resultado -> reeduce(Funcion, lista)
# 4. Min -> Identifica el número mínimo de una lista -> min(Función, lista)
# 5. Pow -> Aplica el exponente a un número -> pow(numero, exponente)
# 6. Sorted -> Ordena los elementos de un lista o tupla -> sorted(lista) -> Para ordenar al rever se usa -> sorted(lista, reverse = True)
